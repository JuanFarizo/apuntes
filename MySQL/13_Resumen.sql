Estructura de Select completa (con partes optativas)

SELECT Lista de seleccion
FROM Origen de los datos (tablas involucradas)
INNER JOIN Juntas Optativas
WHERE Condicion de seleccion de las filas con AND y OR
GROUP BY Columnas de agrupacion
HAVING  Condicion de seleccion del group by
ORDER BY Columnas de ordenacion LIMIT Y OFFSET 